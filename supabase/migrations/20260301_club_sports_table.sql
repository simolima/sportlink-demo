-- INSERT: solo se sei Admin attivo di quel club
DROP POLICY IF EXISTS "Club admin can insert club sports" ON public.club_sports;
CREATE POLICY "Club admin can insert club sports"
    ON public.club_sports FOR INSERT
    TO authenticated
    WITH CHECK (
        EXISTS (
            SELECT 1 FROM public.club_memberships cm
            WHERE cm.club_id  = club_sports.club_id
              AND cm.user_id  = auth.uid()
              AND cm.club_role = 'Admin'
              AND cm.status   = 'active'
        )
    );

