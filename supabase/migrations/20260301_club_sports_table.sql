-- 1. Tabella join club_sports
CREATE TABLE IF NOT EXISTS public.club_sports (
    id          bigint GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    club_id     uuid NOT NULL REFERENCES public.clubs(id) ON DELETE CASCADE,
    sport_id    bigint NOT NULL REFERENCES public.lookup_sports(id) ON DELETE CASCADE,
    created_at  timestamptz NOT NULL DEFAULT now(),

    CONSTRAINT uq_club_sport UNIQUE (club_id, sport_id)
);

CREATE INDEX IF NOT EXISTS idx_club_sports_club ON public.club_sports (club_id);
CREATE INDEX IF NOT EXISTS idx_club_sports_sport ON public.club_sports (sport_id);

-- 2. RLS
ALTER TABLE public.club_sports ENABLE ROW LEVEL SECURITY;

